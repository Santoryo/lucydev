    <p class="font-bold">What is Ganqing?</p>
	<p>
		The ship name is derived from the first letters of Ganyu's name and the last four letters of
		Keqing's name. However, it's worth noting that the Chinese characters used for the ship name are
		drastically different from the ones used for the concept of 'Gănging' (Bia). Gănging (Bi) holds
		significant importance in Chinese culture, and it is loosely translated as "feeling." It refers
		to a friendship-like feeling that develops between two people, groups, or business partners as
		their relationship deepens. Despite the connection to this concept, the ship name itself is
		based purely on the characters' names.
	</p>

	<p>
		The popularity of Ganqing soared within the Genshin Impact fandom due to several factors.
		Firstly, the game developer, miHoYo, often drops hints and crumbs that fuel fans' enthusiasm for
		certain character pairings. The interactions and chemistry between Ganyu and Keging in the game
		have led fans to imagine them as a potential romantic couple or close friends, adding to the
		allure of the ship. Both Ganyu and Keging are members of the Liyue Qixing, a group of seven
		highly influential individuals in Liyue. Their affiliation with this prominent organization also
		contributes to their fame and shared experiences, making their interactions all the more
		intriguing.
	</p>

	<p>
		Interestingly, the contrasting opinions regarding Rex Lapis, a central character in the game,
		have inspired fans to create light-hearted rivalry stories between Ganyu and Keging. These
		stories often explore how the two characters learn to understand each other better, as hinted in
		their voicelines.
	</p>

	<p>
		Another aspect that fans adore is the combination of their purple and blue aesthetics, which has
		led to the creation of numerous fan arts portraying the characters together. A playful reference
		to the Ganqing ship can be found in Keging's second birthday mail, where she receives qingxin
		flowers, which happen to be one of Ganyu's character ascension materials. This detail further
		strengthens the connection between the two characters and adds to the ship's appeal.
